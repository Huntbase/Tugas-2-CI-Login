        <div class="judul"><b>Cara Mengatasi Ikan Hias Akuarium yang sedang Stress</b><br>
        <img src="/img/Rectangle 42.png"></div><br>
        <table border="0" height="60px" width="980" align="center">
            <tr>
                <td>Memilih ikan hias sebagai hewan peliharaan merupakan keputusan tepat sebab mudah dipelihara. Selain sebagai ajang refreshing,
                    memelihara ikan hias mampu membuat suasana hati menjadi lebih baik. Namun, sama halnya dengan jenis hewan peliharaan lain,
                    ikan hias juga dapat mengalami stres, lho. Oleh sebab itu, penting bagi kamu mengetahui penyebab dan cara mengatasi ikan stres.<br>
                    Cara mengatasi ikan stres tergantung pada penyebabnya. Banyak faktor yang membuat ikan stres, antara lain kondisi air kurang baik, gangguan dari jenis ikan lain,
                    perubahan lingkungan, gangguan dari luar (kebisingan atau pukulan di dinding akuarium), hingga pemberian pakan kurang tepat.<br>
                    Ikan peliharaan yang stres sangat mudah dikenali dari cara berenangnya yang lebih cepat, perubahan warna tubuh menjadi kusam, hingga tidak nafsu makan. Berikut tips mengatasi ikan stress<br><br>
                    <b>1. Mengganti Air</b><br>
                    Salah satu penyebab ikan stres adalah lingkungan tempat tinggal atau air tercemar zat beracun. Selain dari sumber air, zat beracun bisa berasal dari sisa pakan maupun kotoran yang mengendap di bagian dasar
                    tangki sehingga kadar amoniak terlarut mengalami peningkatan. Solusinya adalah dengan mengganti airnya.<br><br>
                    <b>2. Bersihkan Akuarium</b><br>
                    Pembersihan akuarium wajib kamu lakukan secara rutin salah satunya untuk meminimalkan tingkat stres pada ikan. Usai dicuci bersih, kamu harus menjemur akuarium terlebih dahulu barulah diisi air kembali.<br><br>
                    <b>3. Lakukan Karantina</b><br>
                    Karantina ikan stres dapat dilakukan dengan cara merendam ikan pada air bercampur garam dengan konsentrasi yang tinggi selama kurang lebih tiga menit. Selain itu, kamu bisa memakai larutan kalium permanganat serta methilene blue.
                    Jika ingin menggunakan bahan alami, coba manfaatkan cairan daun pepaya.<br><br>
                    <b>4. Pemberian Obat</b><br>
                    Saat ini sudah banyak jenis obat untuk mengatasi ikan stres. Salah satunya sulfamerazin yang  dosisnya 2 gram/10 kg pakan. Pemberian obat ini sebaiknya dilakukan bersama penebaran pakan sehingga ikan tertarik untuk makan.
                    Bila ingin menggunakan bahan alami, kamu dapat memilih daun pepaya, mengkudu, atau ubi jalar.<br><br>
                    <b>5. Penyuntikan</b><br>
                    Penyuntikan memakai teramisin bisa kamu lakukan bila jumlah ikan yang stres sedikit. Metode ini cukup efektif khususnya bagi indukan yang hendak menjalani proses pemijahan guna menurunkan risiko stres. Teramisin perlu dilarutkan lebih dulu ke dalam 1 ml aquabidest
                    sebelum disuntikkan dengan dosis 25 mg/1 kg berat ikan.<br><br>
                    Ketika ikan dalam akuarium tampak gelisah, berenang lebih cepat, atau warna tubuhnya berubah, kamu perlu melakukan beberapa langkah penanganan guna mengurangi tingkat stresnya. Dengan begitu, ikan pun cepat pulih dan kondisinya normal kembali. Selamat mencoba!
